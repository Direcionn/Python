#Conversão de tipos:
preco = 5;
print(preco);

#Int para float
preco = float(preco);
print(preco);

preco = 10.33
print(preco)

#Float para int
preco = int(preco)
print(preco)

#Númerico para string
preco = 10.6
idade = 18
print(str(preco))
print(str(idade))
print(f'A idade é de {idade} e o preço é de {preco}')

print(float(preco + 1))
print(int(idade + 1))

#Entrada de dados e conversão para operações matematicas
nome = input("Informe seu nome: ")
print(f"Seu nome é {nome}")
